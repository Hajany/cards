package io.uhndata.cards;

import org.osgi.service.metatype.annotations.AttributeDefinition;
import org.osgi.service.metatype.annotations.ObjectClassDefinition;

@ObjectClassDefinition(name = "Common import",
    description = "Configuration for the data importers")
public @interface CommonImportConfigDefinition
{
    /** Whether or not to handle scheduling surveys. */
    boolean SURVEY_SCHEDULING_ENABLED = true;

    @AttributeDefinition(name = "Import schedule",
        description = "When enabled, survey scheduling is handled by CARDS.")
    boolean survey_scheduling_enabled() default SURVEY_SCHEDULING_ENABLED;
}
